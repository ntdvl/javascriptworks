console.log(true && false); 
// True ve(&&) false karşılaştırması yaptık. Sonuç false çıktı. 
// Eğer true dönmesi isteniyorsa iki operandın da true olması ya da sonucu true olacak iki işleme sahip olması gerekir
console.log(true || false); 
// 	True veya(||) false karşılaştırması yaptık. Sonuç true çıktı. Herhangi biri true olursa sonuç true olur.